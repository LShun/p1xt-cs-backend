public class Date {
    public static void main(String args[]) {
        System.out.println("Hello World!");

        String day = "Saturday";
        int date = 8;
        String month = "July";
        int year = 2019;

        // American Format
        System.out.println(day + ", " + month + " " + date + ", " + year);  

        // European Format
        System.out.println(day + " " + date + " " + month + " " + year);

        
    }
}